package com.smitcoderx.jomkes;

public class FactTopicModel {

    private int image;
    private String name;

    public FactTopicModel(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
